public class DeclarationAndDefinition {
    public static void main(String arv[])
    {
        // this is a declaration that the variable name 'answer' is of type 'double';
        double answer;
        // this is an assignment, i.e. it assigns a value to the variable
        answer = 42;
        
        // this is a declaration that the variable name question is of type 'int' and
        // and a definition that its value is 42
        int secondAnswer = 42;
        
        System.out.println("The answer is " + answer);
        System.out.println("The second answer is " + secondAnswer);
  
    }
}
